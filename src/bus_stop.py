class BusStop:
    def __init__(self, object_id, county, description, direction_op, dlat_gis, dlong_gis, line, municipality, stop_num, stop_type, street_dir, heading, image, point2id):
        self.object_id = object_id
        self.county = county
        self.description = description
        self.direction_op = direction_op
        self.dlat_gis = dlat_gis
        self.dlong_gis = dlong_gis
        self.line = line
        self.municipality = municipality
        self.stop_num = stop_num
        self.stop_type = stop_type
        self.street_dir = street_dir
        self.heading = heading
        self.image = image
        self.point2id = point2id
